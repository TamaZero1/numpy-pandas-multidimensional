import numpy as np

# Crear un arreglo simple
arr = np.array([1, 2, 3, 4, 5])
print("Arreglo simple:", arr)

# Crear un arreglo de ceros
arr_zeros = np.zeros(5)
print("Arreglo de ceros:", arr_zeros)

# Crear un arreglo de unos
arr_ones = np.ones(5)
print("Arreglo de unos:", arr_ones)

# Crear un arreglo con valores espaciados uniformemente
arr_linspace = np.linspace(0, 10, 5)
print("Arreglo espaciado uniformemente:", arr_linspace)